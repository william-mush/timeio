// Helper function to get city by slug
export function getCityBySlug(slug: string): USCity | undefined {
  return US_CITIES_OVER_100K.find(city => city.slug.toLowerCase() === slug.toLowerCase());
}

// Helper function to search cities by name
export function searchCities(query: string): USCity[] {
  if (!query.trim()) return [];
  
  const searchTerm = query.toLowerCase();
  return US_CITIES_OVER_100K.filter(city => 
    city.name.toLowerCase().includes(searchTerm) || 
    city.state.toLowerCase().includes(searchTerm)
  );
}

// Get all unique states
export function getStates(): string[] {
  const states = Array.from(new Set(US_CITIES_OVER_100K.map(city => city.state)));
  return states.sort();
}

// Get cities by state
export function getCitiesByState(state: string): USCity[] {
  return US_CITIES_OVER_100K.filter(city => 
    city.state.toLowerCase() === state.toLowerCase()
  );
}